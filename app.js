document.querySelector(".apply").addEventListener("click", () => {
  const selected = document.querySelector('input[name="sort"]:checked').value;

  let result = [...restaurant];

  if (selected === "ratingHigh") {
    result.sort((a, b) => b.rating - a.rating);
  }

  if (selected === "costLow") {
    result.sort((a, b) => a.Price_for_two - b.Price_for_two);
  }

  if (selected === "costHigh") {
    result.sort((a, b) => b.Price_for_two - a.Price_for_two);
  }

  if (selected === "distance") {
    result.sort(
      (a, b) =>
        Number(a.distance_from_customer_house) -
        Number(b.distance_from_customer_house)
    );
  }

  document.getElementById("root").innerHTML = "";
  getrestaurant(result);

  document.getElementById("filterPopup").classList.remove("active");
});
